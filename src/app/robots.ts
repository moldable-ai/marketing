import { MetadataRoute } from "next";

const APP_URL = process.env.NEXT_PUBLIC_APP_URL || "https://moldable.sh";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
        disallow: [
          // API routes
          "/api/",
          // Download redirect routes
          "/download/",
        ],
      },
    ],
    sitemap: `${APP_URL}/sitemap.xml`,
  };
}
